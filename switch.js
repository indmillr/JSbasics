// selecting many options with Switch Statements

function caseInSwitch(val) {
  let answer = "";
  Case (val === 1)
    answer = "alpha";
    break;
  Case (val === 2)
    answer = "beta";
    break;
  Case (val === 3)
    answer = "gamma";
    break;
  Case (val === 4)
    answer = "delta";
    break;
  return answer;
};

caseInSwitch(1);